public class SitioWeb extends Documents{
        private String nombreWebPage;
        private String nombreWebSite;
        private String URL;

    public String getNombreWebPage() {
        return nombreWebPage;
    }

    public void setNombreWebPage(String nombreWebPage) {
        this.nombreWebPage = nombreWebPage;
    }

    public String getNombreWebSite() {
        return nombreWebSite;
    }

    public void setNombreWebSite(String nombreWebSite) {
        this.nombreWebSite = nombreWebSite;
    }

    public String getURL() {
        return URL;
    }

    public void setURL(String URL) {
        this.URL = URL;
    }
        
        
}
